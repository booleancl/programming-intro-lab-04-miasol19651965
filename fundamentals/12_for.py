# El for es una estructura de control que nos permite realizar la misma accion con elementos diferentes cada vez

veggies = ['Broccolli', 'Celery', 'Lettuce', 'carrot']

for veggie in veggies:
    print('I need to eat',veggie)

fruits = ['Apple','banana', 'orange', 'Strawberry']

for fruit in fruits:
    print('I love to eat', fruit)