# Todos estos valores pertenecen a algun tipo
# para python los tipos son las denominadas clases
# estos tres ejemplos son respectivamente

# int: numeros enteros sin parte fraccionaria
print(type(2))
# float: para numeros con parte fraccionaria
print(type(7.0))
# str: para las cadenas de caracteres o simplemente texto (tring)
print(type("hola mundo"))